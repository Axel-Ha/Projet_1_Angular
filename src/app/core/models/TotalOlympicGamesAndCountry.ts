/**
 * Represents the overall statistics of the Olympic Games, including the total number of Games and the number of participating countries.
 */
export interface TotalOlympicGamesAndCountry {
    /**
     * The total number of Olympic Games held.
     */
    totalOlympicGames: number;

    /**
     * The total number of countries that have participated in the Olympic Games.
     */
    totalCountries: number;
}
